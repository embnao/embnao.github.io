#include <stdio.h>

/*
	for (@1; @2; @3) {
		@4;
	}
	@1:循环变量的初始化,只在循环第一次执行的时候执行一次
	@2:循环条件,成立则执行@4
	@4:循环体,执行结束到@3
	@3:循环变量的改变
	@1 ---> @2 -成立-->@4--->@3--->@2-成立-->@4--->@3-->@2--->@4....
 
 */
int main(void)
{
	int i;
	int sum = 0;

	// 100的前n项和
	for (i = 1; i <= 100; i++ /*i += 1*/) {
		sum += i;
	}

	printf("100的前n项和为:%d\n", sum);

	return 0;
}

